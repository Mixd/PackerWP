/**
 * Load database variables for a given stage
 *
 * @param string $stage
 * @return array
 */
function getDatabaseVars($stage)
{
    $database = json_decode(file_get_contents(realpath(__DIR__) . '/.config/database.json'), true);
    return $database[$stage];
}
